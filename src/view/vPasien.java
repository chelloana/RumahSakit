    private void tabelpasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpasienMouseClicked
        int baris = tabelpasien.getSelectedRow();
        tidpasien.setText(tabelpasien.getValueAt(baris, 0).toString());
        tnamapasien.setText(tabelpasien.getValueAt(baris, 1).toString());
        ttlppasien.setText(tabelpasien.getValueAt(baris, 2).toString());
        
        String tanggal = tabelpasien.getValueAt(baris, 3).toString();
        try {
            SimpleDateFormat dateFormatTabel = new SimpleDateFormat("yyyy-MM-dd"); // Format tanggal dari tabel
            SimpleDateFormat dateFormatChooser = new SimpleDateFormat("dd-MM-yyyy "); // Format tanggal untuk JDateChooser

            Date parsedDate = dateFormatTabel.parse(tanggal);
            String formattedDate = dateFormatChooser.format(parsedDate);

            Date date = dateFormatChooser.parse(formattedDate);
            //jDateChooser.setDate(date);
            ttanggalpasien.setDate(date);
        } catch (ParseException e) {
            e.printStackTrace();
}

        talamatpasien.setText(tabelpasien.getValueAt(baris, 4).toString());
        
        String jenis = tabelpasien.getValueAt(baris, 5).toString();
        cbjenis.setSelectedItem(jenis);

    }//GEN-LAST:event_tabelpasienMouseClicked

