    private void BtnOptionalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnOptionalActionPerformed
        // TODO add your handling code here:
        if (posisi == 0){
            if(getTidpasien().getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Pilih pasien terlebih dahulu");
            } else{
                new vRiwayat(posisi).setVisible(true);
                this.dispose();
            }
        } 
        else if (posisi == 2){
            if (getTidpasien().getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Pilih pasien terlebih dahulu");
            } else{
                new vPendaftaran(posisi, Integer.parseInt(tidpasien.getText()), tnamapasien.getText()).setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_BtnOptionalActionPerformed

